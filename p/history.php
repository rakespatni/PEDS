  					<h2><u>History of Physical Education Department<u></h2><br>
					<br>
					
					<p><h4>The  department of physical education had existed right from the inception of Calicut REC in 1962. The department had one teacher, Mr. K M K Ahamed with 2 markers to assist him. Mr. Ahamed tragically passed away due to illness while in service and other subject teachers were standing in temporarily till 2 regular Lecturers namely,  M/S  T.M. Abdurahiman and John Philip, took charge in 1971.
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;Dr. A.M. Najeeb joined the department in October,1976 in place of John Philip who opted out of the institution for domestic reasons. Sri. Abdurahiman retired as Asst. Professor in  July, 2002 and was  replaced by  Mr. Sunil M.S who joined as Lecturer in August 2005. The duo of Dr. Najeeb and Mr. Sunil continues as on date. The two regular markers in the department M/S Shanku Francis and K.Ramadasan also retired from service during 1999 and 2005 respectively. They have not been replaced yet and the department is makeshifting with temporary hands.
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;During (Late) Mr. Ahamed's time, CREC was affiliated to Kerala University and  shifted to University of Calicut when the latter was formed in 1969. Those days, we had excellent cricket players, the most outstanding of them all, Mr. Kalyan Sunderam who went on to play for South zone in Duleep trophy. There were also others like Roy Chaudhuri, Mohan Mathen, P V Surendran, Venugopal, who made their mark in University and state cricket. There was one year when 6 of our students figured in the Kerala Ranji Trophy side. Lack of facilities may have been the reason for our confinement to limited activities during those days. But in what ever we participated we made a mark. Later on during Abdurahiman and John Philip, the Canvas got wider and the participation spread to almost all disciplines in the university curriculum. The duo laid the real foundation for sports in CREC enhancing the number of facilities as well. The present foot ball field(now gone to accommodate a classroom complex), Cricket stadium at XII mile, Tennis complex, Hockey field etc. were formed during this period. Cricket stadium went global with the addition of turf wicket facility in 1992 and national level Ranji matches hosted on campus in 1992, 1994, 1999 and 2000. Notable international players who have played on the CREC soil are Mohamed Azharuddin, Venkatesh Prasad, Kirmani to name a few. A full fledged Gymnsium together with a swimming pool were added in 1996. An Indoor stadium, Outdoor Sports Complex, Cricket Pavilion etc. are on the cards.
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;The period between 1975-76 to 1987-88 can be considered as the golden era of sports in CREC. We were the best college in sports under Calicut University during the period. We won the titles 6 times in a row and when ever we were not first, we were a close second. We boasted of the best teams in Cricket, Basket ball, Foot ball, Wrestling, Weightlifing, Boxing, Table tennis, Tennis, Badminton and so on. Some outstanding sports stars who reigned supreme during this period were many like: K.T.J. Mathews, (Indian Universities Captain), Mohanan, Minhajul Haque, M.Saleem and so on in foot ball; Sudhakar, Abdul Majeed, Mani Benjamin, Derrick Samuel, P T Chacko, Chellam Pillai, John Joseph, Philip Zacharia and so on in Basket ball; Siby Isaac(State Cricketer who scored a double hundred against Andhra), M D Shridhar, Premkumar, Swami Viswanath, Kasi Viswanath, Susheel Dhar, Anand Kumar, D. Balashankar, Jayafar Moidu Venugopal,  Thomas, Banerjee,  Soman Dutta, Vijayan,  Ganesh, Devanand, Vijayakrishnan , Mukesh Sharma and so on in cricket; Abdul Naser(Prolific scorer for Kerala), Sanjeev, Vijayaraj, Jaishanker, sarto Irudayaraj and so on in Hockey; V. Babu, J M Shankar, Sunil kanaran in Athletics;  John of Matha(National Double champion), Thomas Mathew, S L Kumar and so on in Badminton; Sreenivasan( Indian colours in Commonwealth Games), Sebastian, Manjit Ghuman and so on in Table tennis; Harrid, Mohan Raj and so on in Tennis.
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;The year 1987 saw us hosting the biggest event on the campus to celebrate our silver jubilee, the Calicut University Athletic Meet and who else but the great P.T. Usha being the attraction of  the meet.
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;In 1992, we conducted in our Auditorium the South India zone Interuniversity Table tennis for men and women. The Calicut University men team were the champions which boasted of four players from our college.
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;In 1992 we also laid our first turf wicket on our cricket stadium and inaugurated it with a national level Ranji trophy match, Kerala Vs- Karnataka thus finding a place on the national cricket map. In 1994, a second turf strip was added prior to 2 Ranji matches against Goa and Karnataka. The last national level matches were in 1999-2000, against Hyderabad and Andhra which saw the likes of Azharuddin playing on our ground. Lack of modern Players' Dressing Rooms has prevented any further match from coming to our ground.Today in the Golden Jubilee year, we possess one of the best cricket stadia with all amenities and soon we will be staging major matches.
</h4></p><br />
 
 					<h2><u>Objectives and Motto</u><h2><br>
 					<p><h4>Right from inception, the department of physical education has maintained its goal and motto as  Total Fitness on the Campus. It still remains so in the NITC mode.
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;Till the year 2002, as Calicut REC under University of Calicut, Physical education was a Voluntary subject with only those students who wished participated. But the cream of sports men and women participated and good teams were prepared and fielded for university, district, state and national tournaments with satisfactory results. The students then enjoyed incentives like duty leaves and sports sessional marks which certainly motivated them. How ever, we were never near our objective of total fitness due to smaller participation level. This, we tried making up with campus opens with small amount of success.
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;The transformation to NIT Calicut has brought about a vast change with Physical education finding its rightful place in the curriculum and students flocking in great numbers to the sports infrastructure. The condition has become so overwhelming that no undergraduate student leaves the institute with out once passing through the corridors of physical education. The desired quantum of participation has been achieved and we can proudly boast that we have  very nearly  accomplished  our objective. How ever, the participation of our teams in external tournaments has come down considerably. Academic constraints together with the lack of incentives prevent even good performers from taking part in competitive events. But on-campus activities has never been so good.</h4></p>
 
